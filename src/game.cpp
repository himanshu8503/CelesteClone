void update_game()
{
    for(int i = 0; i < 10; i++)
    {
        for(int j = 0; j < 10 ; j++)
        {
            draw_sprite(SPRITE_DICE,{i *100.0f, j * 100.0f},{100.0f,100.0f});
        }
    }
}